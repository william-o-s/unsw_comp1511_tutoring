//
// Face 2:
//  ~ ~
//  0 0
//   o
//  \_/

#include <stdio.h>

int main(void) {
    
    // Face 1
    printf("  ~ ~\n");
    printf("  0 0\n");
    printf("   o\n");
    printf("   -\n");
    
    // Face 2
    printf("  ~ ~\n");
    printf("  0 0\n");
    printf("   o\n");
    printf("  \\_/\n");

    return 0;
}
